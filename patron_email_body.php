    <h2 style="font-style: oblique; font-size: large">Policies – Room Reservations</h2>

    <p>
        New Mexico Tech Skeen Library offers four rooms for reservation by groups. Reservations must be made at least 24
        hours in advance.
    </p>

    <p>
        <em>In the case of a scheduling conflict, New Mexico Tech events will be given priority.</em>
    </p>

    <h3>Cost</h3>

    <ul>
        <li>New Mexico Tech affiliated users and federal and state-funded government organizations may use
            facilities at no charge. (Proof of affiliation is required for reservations)
        </li>
        <li>Other users not affiliated with New Mexico Tech may rent Room 212 at $10 per hour and rooms 5 and 8 at $5
            per hour.
        </li>
        <li>Reservation fees are due at the time of room use.</li>
        <li>Commercial events are not permitted at the library. Events must be free and no goods or services can be
            sold at the library.
        </li>
    </ul>

    <h3>Setup</h3>
    <ul>
        <li>Groups are responsible for configuring the furniture to their liking. Library staff will aid in setting up
            any
            reserved audio/visual equipment. Groups must vacate meeting rooms 15 minutes before the Library closes.
        </li>
        <li>(Room 212) The person responsible for the group must check in at the Circulation Desk upon arrival to have
            the staff unlock and check the room. They must also inform the staff when the group is ready to leave and
            check
            out with the staff.
        </li>
    </ul>

    <h3>Fair Use of Library Resources</h3>
    <p>
        The Library provides rooms as a community service. If your group fails to use an ongoing room
        reservation more than 3 times, your reservation may be canceled. If the room or equipment is damaged, you may
        lose the privilege of reserving rooms at the library.
    </p>

    <h3>Catering</h3>
    <p>
        The consumption of food and drink is allowed throughout the Library. Groups desiring catered service
        may contact <a href="http://www.dineoncampus.com/nmt/show.cfm?cmd=catering">Chartwells</a> at 575‑835‑5111.
    </p>

    <h3>Internet</h3>
    <p>
        Groups desiring internet usage beyond the Library’s single computer in Room 212 need to contact <a
            href="http://www.nmt.edu/information-services-department">ITC</a> for
        permission, passwords, cost estimations, etc. at 575‑835‑5700. (Library staff will not arrange internet
        access)
    </p>


    <p style="font-style:italic">
        New Mexico Tech Library will not provide a forum for groups advocating illegal activities.
